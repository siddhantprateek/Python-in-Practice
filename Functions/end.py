for i in range(0, 10, 2):
    print(i, end=" ")
# print function in python is by default jumps to next line, so to continue printing same line
# or next line as per user. he provide a keyword called <end>.
# end=" "


# The default value of end is \n meaning that after the print statement
# it will print a new line. So simply stated end is what you want to be
# printed after the print statement has been executed


# The print() function inserts a new line at the end, by default.
# In Python 2, it can be suppressed by putting ',' at the end.
# In Python 3, "end =' '" appends space instead of newline.
