# The default value of end is \n meaning that after the print statement
# it will print a new line. So simply stated end is what you want to be
# printed after the print statement has been executed

print("the end is near", end=" ") or print("the end is near", end="\n")


# Python’s print() function comes with a parameter called ‘end’. By default,
#  the value of this parameter is ‘\n’, i.e. the new line character.
#  You can end a print statement with any character/string using this parameter.

print("Hi", end=' ')
print("Jhon", end=' ')
